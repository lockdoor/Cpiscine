char	*ft_strncat(char *dest, char *src, unsigned int n)
{
	char	*addr;

	addr = dest;
	while (*dest)
		dest++ ;
	while (*src && n--)
		*dest++ = *src++;
	if (!n)
		*dest = '\0';
	return (addr);
}
