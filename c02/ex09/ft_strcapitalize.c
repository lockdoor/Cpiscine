#include <stdio.h>

char	*ft_strcapitalize(char *str)
{
	char	*s;

	s = str;
	while (*s)
	{
		if (!(*(s - 1) >= 'A' && *(s - 1) <= 'Z')
			&& !(*(s - 1) >= 'a' && *(s - 1) <= 'z')
			&& !(*(s - 1) >= '0' && *(s - 1) <= '9')
			&& (*(s) >= 'a' && *(s) <= 'z'))
			*s -= 32;
		s++ ;
	}
	return (str);
}
