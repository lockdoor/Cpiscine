int	ft_atoi(char *str)
{
	int	n;
	int	pos;

	n = 0;
	pos = 1;
	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	while (*str == '+' || *str == '-')
	{
		if (*str == '-')
			pos *= -1;
		str++ ;
	}
	while (*str >= '0' && *str <= '9')
	{
		n = (n * 10) + (*str - '0');
		str++ ;
	}
	return (n * pos);
}
