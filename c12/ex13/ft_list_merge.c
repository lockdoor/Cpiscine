#include "ft_list.h"

void	ft_list_merge(t_list **begin_list1, t_list *begin_list2)
{
	t_list	*curent;

	curent = *begin_list1;
	while (curent->next)
		curent = curent->next;
	curent->next = begin_list2;
}
