#include "ft_list.h"

void	ft_list_clear(t_list *begin_list, void (*free_fct)(void *))
{
	t_list	*current;

	while (begin_list)
	{
		current = begin_list;
		begin_list = begin_list->next;
		free_fct (current->data);
		free_fct (current);
	}
}