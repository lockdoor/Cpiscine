#include <unistd.h>

void	ft_putchar(char c)
{
	write (1, &c, 1);
}

void	put_number(int n)
{
	ft_putchar ((n / 10) + '0');
	ft_putchar ((n % 10) + '0');
}

void	ft_print_comb2(void)
{
	int	a;
	int	b;

	a = 0;
	while (a <= 98)
	{
		b = a + 1;
		while (b <= 99)
		{
			put_number (a);
			ft_putchar (' ');
			put_number (b);
			if (a < 98)
				write (1, ", ", 1);
			++b ;
		}
		++a ;
	}	
}
