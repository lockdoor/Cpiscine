void	ft_swap(int *a, int *b)
{
	int	temp;

	temp = *a;
	*a = *b;
	*b = temp;
}

void	ft_sort_int_tab(int *tab, int size)
{
	int	index;

	index = 0;
	while (index < size - 1)
	{
		if (tab[index] > tab[index + 1])
		{
			ft_swap (&tab[index], &tab[index + 1]);
			if (index > 0)
			{
				--index ;
				continue ;
			}
		}
		++index ;
	}
}
